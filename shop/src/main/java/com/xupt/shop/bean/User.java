package com.xupt.shop.bean;

public class User {
    private int id;
    private String name;
    private int type;//1普通用户 2管理员 0 商家

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
